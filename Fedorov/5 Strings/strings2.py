s = "Pythons are found in Africa"
print("s:", s)

print("s{0]:", s[0])

print("s{-1]:", s[-1]) # последний символ в строке

print("s[1:3]:" + s[1:3])

print("s[:3]:", s[:3])  # с 0 индекса по 3-ий не включительно

print("s[:]:", s[:])    # вся строка

print("s[::2]:", s[::2]) # третий аргумент задает шаг (по умолчанию один)

print("s[::-1]:",s[::-1]) # «обратный» шаг

print("s[:-1]:",s[:-1]) # вспомним, как мы находили отрицательный индекс


    
